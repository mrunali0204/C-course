#include<stdio.h>

int main(void)
{
    printf("1. %d\n", 10 < 20);             //1
    printf("2. %d\n", 20 < 20);             //0
    printf("3. %d\n", 20 <= 20);             //1
    printf("4. %d\n", 20 >= 20);             //1
    printf("5. %d\n", 20 != 20);             //0
    printf("6. %d\n", 20 != 10);             //1
    printf("7. %d\n", 20 == 20);             //1
    printf("8. %d\n", 20 == 10);             //0

    printf("9. %d\n", 10 <= 20 != 0);             //1
    printf("10. %d\n", 20 < 20 != 1);             //1
    printf("11. %d\n", 10 > 10 != 0);             //0
    printf("12. %d\n", 20 == 10 != 0);             //0

    printf("13. %d\n", 20 >= 10 < 0);             //0
    printf("14. %d\n", 20 > 10 >= 0);             //1

    return 0;

}

